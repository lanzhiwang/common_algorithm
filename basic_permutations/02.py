def print_99_2(num):
    if num < 1 or num > 9:
        return ''
    temp = ''
    for i in range(1, num + 1):
        temp += ('%s*%s=%s ' % (num, i, num * i))
    if num == 9:
        return temp
    return temp + '\n' + print_99_2(num + 1)


